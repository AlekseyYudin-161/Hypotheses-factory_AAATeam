"""
Pydantic-контракт «Фабрики гипотез».

Здесь описаны ВСЕ структуры данных, которыми обмениваются шаги пайплайна.
Схемы служат одновременно:
  1) валидацией входа/выхода;
  2) `response_format` для LLM (`with_structured_output`) — описания в `Field`
     это фактически инструкция модели, поэтому они написаны максимально явно.

Домен: обогащение руд и металлургия (переработка хвостов, доизвлечение Ni/Cu).
"""

from __future__ import annotations

from typing import List, Optional

from pydantic import BaseModel, Field


# ---------------------------------------------------------------------------
# ШАГ 1. Индустриальная декомпозиция «богатого входа»
# ---------------------------------------------------------------------------
class IndustrialConstraints(BaseModel):
    """
    Результат разбора «богатого промпта» жюри.

    Разделение на white/black-list — ключевая идея ретривала:
      * target_material уходит в BLACK-LIST (не ищем совпадения по «мусору»/отходам,
        иначе поиск вернёт статьи про сами хвосты, а не про способ их переработки);
      * target_property уходит в WHITE-LIST (ищем только то, что реально помогает
        достичь цели — например «доизвлечение никеля»).
    """

    target_material: str = Field(
        description=(
            "Целевой материал / отход, который мы перерабатываем "
            "(например, 'отвальные пирротиновые хвосты'). Пойдёт в BLACK-LIST поиска."
        )
    )
    target_property: str = Field(
        description=(
            "Целевое улучшение процесса (например, 'доизвлечение никеля', "
            "'снижение расхода реагентов'). Пойдёт в WHITE-LIST поиска."
        )
    )
    budget: Optional[float] = Field(
        default=None,
        description="Бюджет на реализацию в условных денежных единицах. None — если не задан.",
    )
    time_limit: Optional[str] = Field(
        default=None,
        description="Ограничение по срокам, как указано в задании (например, '3 месяца').",
    )
    mass_smt: Optional[float] = Field(
        default=None,
        description=(
            "Масса перерабатываемого потока в СМТ (сухих метрических тоннах). "
            "Нужна экономисту для расчёта рентабельности доизвлечения."
        ),
    )
    mechanisms: List[str] = Field(
        default_factory=list,
        description=(
            "Список конкретных СУЩНОСТЕЙ для векторного поиска: реагенты, аппараты, "
            "физико-химические механизмы (например, 'автоклавное выщелачивание', "
            "'ксантогенат', 'магнитная сепарация'). Не действия, а сущности."
        ),
    )


# ---------------------------------------------------------------------------
# Причинно-следственная цепочка в формате PMDco
# ---------------------------------------------------------------------------
class CausalLink(BaseModel):
    """
    Одно звено цепочки по стандарту PMDco: Entity -> Activity -> Entity.

    То есть: некое Сырьё (Entity) через Процесс (Activity) превращается
    в Продукт (Entity). Несколько таких звеньев образуют полную цепочку передела.
    """

    entity_in: str = Field(description="Входная сущность / сырьё (Entity).")
    activity: str = Field(description="Процесс / операция передела (Activity).")
    entity_out: str = Field(description="Выходная сущность / продукт (Entity).")

    def as_arrow(self) -> str:
        """Человекочитаемое представление звена для UI/логов."""
        return f"{self.entity_in} -[{self.activity}]-> {self.entity_out}"


# ---------------------------------------------------------------------------
# ШАГ 3. Черновик гипотезы (то, что генерирует LLM за ОДИН вызов)
# ---------------------------------------------------------------------------
class HypothesisDraft(BaseModel):
    """Сырая гипотеза от LLM ещё без численного скоринга (шаги 4 добавят метрики)."""

    hypothesis_text: str = Field(
        description=(
            "Текст гипотезы: что предлагаем сделать и за счёт какой физики/химии это "
            "повысит доизвлечение или снизит затраты. Обязательно уложиться в бюджет."
        )
    )
    causal_chain: List[CausalLink] = Field(
        description=(
            "Причинно-следственная цепочка передела в формате PMDco "
            "[Сырьё(Entity) -> Процесс(Activity) -> Продукт(Entity)]."
        )
    )
    key_process: str = Field(
        description=(
            "Ключевой процесс/аппарат гипотезы одним термином (например "
            "'автоклавное выщелачивание'). Передаётся экономисту для оценки риска."
        )
    )
    doi_sources: List[str] = Field(
        description="Список DOI из предоставленного контекста. Брать ТОЛЬКО из [DOI: ...] в контексте.",
    )
    expected_kpi_gain: float = Field(
        ge=0.0,
        le=1.0,
        description=(
            "Оценка LLM теоретического прироста KPI по контексту, 0..1 "
            "(0 — эффекта нет, 1 — максимальный обоснованный прирост). Это Value Score."
        ),
    )


class HypothesisBatch(BaseModel):
    """
    Контейнер для КАП генерации: LLM обязана вернуть сразу топ-3 гипотезы
    за один вызов (экономим на round-trip'ах под 2-минутный таймаут).
    """

    hypotheses: List[HypothesisDraft] = Field(
        description="Ровно 3 наиболее перспективные гипотезы, отсортированные по перспективности."
    )


# ---------------------------------------------------------------------------
# ШАГ 4. Финальная отранжированная гипотеза (выход пайплайна)
# ---------------------------------------------------------------------------
class Hypothesis(HypothesisDraft):
    """Гипотеза с посчитанными осями Risk / Value / Novelty и агрегированным скором."""

    risk_score: float = Field(description="Экономический/технологический риск, 0..1 (выше — хуже).")
    value_score: float = Field(description="Теоретический прирост KPI, 0..1 (выше — лучше).")
    novelty_score: float = Field(description="Новизна по совпадению триплетов в базе, 0..1 (выше — новее).")
    final_score: float = Field(description="Агрегированный балл, 0..1. По нему идёт ранжирование.")


# ---------------------------------------------------------------------------
# Результат всего пайплайна: либо гипотезы, либо легальный отказ
# ---------------------------------------------------------------------------
class PipelineResult(BaseModel):
    """
    Итог работы `FabrikaPipeline.generate`.

    `refused=True` означает легальный отказ по ТЗ: ограничения слишком жёсткие
    (нет бюджета / всё нерентабельно) и `hypotheses` пуст.
    """

    hypotheses: List[Hypothesis] = Field(default_factory=list)
    refused: bool = False
    message: Optional[str] = None
